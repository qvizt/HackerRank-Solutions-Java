# Solution for Day 24 More Linked Lists

class Node:
    def __init__(self, data):
        self.data = data
        self.next = None


class Solution:
    def insert(self, head, data):
        p = Node(data)
        if head == None:
            head = p
        elif head.next == None:
            head.next = p
        else:
            start = head
            while (start.next != None):
                start = start.next
            start.next = p
        return head

    def display(self, head):
        current = head
        while current:
            print(current.data, end=' ')
            current = current.next

    def removeDuplicates(self, head):
        values = set()
        values.add(head.data)
        node = head
        while node.next:
            if node.next.data in values:
                node.next = node.next.next
            else:
                node = node.next
                values.add(node.data)

        return head


